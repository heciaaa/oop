package Basics;

public class BIT{
    public static void main(String[] args) {
        int a = 10;
        int b = 100;
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(a>>2);
        System.out.println(b<<2);
    }
}